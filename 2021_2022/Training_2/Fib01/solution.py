numbers = [1, 1]
for i in range(2, 11):
	numbers.append(numbers[i - 1] + numbers[i - 2])


def decode(string):
	rev = string[::-1]
	code = 0
	for i in range(len(rev)):
		code += int(rev[i]) * numbers[i]
	return code


